x = [[0] * 10 for i in range(10)]

for i in range(10):
    a = list(map(int, input().split()))
    for j in range(10):
        x[i][j] = a[j]

nowx = 1
nowy = 1
while(True):
    if x[nowx][nowy] == 2 or (nowx == 9 and nowy == 9):
        x[nowx][nowy] = 9
        break
    if x[nowx][nowy + 1] == 1 and x[nowx + 1][nowy] == 1:
        x[nowx][nowy] = 9
        break
    x[nowx][nowy] = 9
    if x[nowx][nowy + 1] == 1:
        nowx += 1
    else:
        nowy += 1

for i in range(10):
    for j in range(10):
        print(x[i][j], end=' ')
    print()